
public class Airline {
	
	private String code;
	private String name;
	
	public void setCode(String newCode){
		code = newCode;
	}
	
	public void setName(String newName){
		name = newName;
	}
	
	public String getCode(){
		return code;
	}
	
	public String getName(){
		return name;
	}
	
		// Airline Constructor
	public Airline(){}

}
