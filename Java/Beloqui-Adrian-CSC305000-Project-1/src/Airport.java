
public class Airport {
	
	private String code;
	private String city;
	
	public void setCode(String newCode){
		code = newCode;
	}
	
	public void setCity(String newCity){
		city = newCity;
	}
	
	public String getCode(){
		return code;
	}
	
	public String getCity(){
		return city;
	}
	
		// Airport Constructor
	public Airport(){}

}
